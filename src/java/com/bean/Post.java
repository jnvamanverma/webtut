package com.bean;

import java.util.Date;

/**
 *
 * @author jnvam
 */
public class Post {
    private int id, catId;
    private String title, author, body, tags, category;
    private Date date;

    public Post() {
    }

    public Post(int id, String category, String title, String author, String body, String tags, int catId, Date date) {
        this.id = id;
        this.category = category;
        this.title = title;
        this.author = author;
        this.body = body;
        this.tags = tags;
        this.catId = catId;
        this.date = date;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public String getTags() {
        return tags;
    }

    public int getCatId() {
        return catId;
    }

    public void setCatId(int catId) {
        this.catId = catId;
    }
    
    public void setTags(String tags) {
        this.tags = tags;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }
    
}
